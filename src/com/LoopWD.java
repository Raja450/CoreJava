package com;

public class LoopWD {
	public static void main(String[] args) {
		// while loop
		int i = 1;
		while (i <= 10) {
			System.out.println(i);
			i++;
		}
		System.out.println("do while ");
		// do while loop
		int j = 1;
		do {
			System.out.println(j);
			j++;

		} while (j <= 10);
	}

}